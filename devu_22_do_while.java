package com.company;

public class devu_22_do_while {
    public static void main(String[] args) {
        int b = 0;
        do {
            System.out.println(b);
            b++;
        }while (b<5);

        int c = 10;
        do {
            System.out.println(c);
            c--;
        }while (c>0);

    }
}
